const a1 = 3;
const b1 = 40;
const c1 = 31;
const d1 = 45;


const a2 = 19;
const b2 = 53;
const c2 = 11;
const d2 = 22;

function exibirSoma (V1, V2) {
    console.log (V1 + V2);
}

exibirSoma(a1, b1);
exibirSoma(c1, d1); 

function subtrair (V1, V2) {
    console.log (V1 - V2);
}

subtrair(a2, b2);
subtrair(c2, d2);